public class NewProjectObject {

    private String memberRequired, problemStatement, projectName, researchedWork, solutionOffered, submittedBy, existingSolution;
    private int numberOfPlusOnes, comments;

    public String getMemberRequired() {
        return memberRequired;
    }

    public void setMemberRequired(String memberRequired) {
        this.memberRequired = memberRequired;
    }

    public String getProblemStatement() {
        return problemStatement;
    }

    public void setProblemStatement(String problemStatement) {
        this.problemStatement = problemStatement;
    }

    public String getProjectName() {
        return projectName;
    }

    public void setProjectName(String projectName) {
        this.projectName = projectName;
    }

    public String getResearchedWork() {
        return researchedWork;
    }

    public void setResearchedWork(String researchedWork) {
        this.researchedWork = researchedWork;
    }

    public String getSolutionOffered() {
        return solutionOffered;
    }

    public void setSolutionOffered(String solutionOffered) {
        this.solutionOffered = solutionOffered;
    }

    public String getSubmittedBy() {
        return submittedBy;
    }

    public void setSubmittedBy(String submittedBy) {
        this.submittedBy = submittedBy;
    }

    public String getExistingSolution() {
        return existingSolution;
    }

    public void setExistingSolution(String existingSolution) {
        this.existingSolution = existingSolution;
    }

    public int getNumberOfPlusOnes() {
        return numberOfPlusOnes;
    }

    public void setNumberOfPlusOnes(int numberOfPlusOnes) {
        this.numberOfPlusOnes = numberOfPlusOnes;
    }

    public int getComments() {
        return comments;
    }

    public void setComments(int comments) {
        this.comments = comments;
    }



}
